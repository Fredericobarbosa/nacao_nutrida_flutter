import 'package:flutter/material.dart';

class AuthManager extends ChangeNotifier {
  // Exemplo de estado de autenticação
  bool _isLoggedIn = false;

  bool get isLoggedIn => _isLoggedIn;

  void login() {
    _isLoggedIn = true;
    notifyListeners();
  }

  void logout() {
    _isLoggedIn = false;
    notifyListeners();
  }
}
